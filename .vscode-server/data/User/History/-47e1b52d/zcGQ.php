<?php
// Проверяем, существует ли файл с счетчиком
if (!file_exists('counter.txt')) {
    // Если нет, создаем его и записываем в него значение 1
    file_put_contents('counter.txt', '1');
} else {
    // Если есть, читаем из него значение счетчика
    $counter = file_get_contents('counter.txt');
}

// Проверяем, является ли текущее обновление страницы первым
if (!isset($_COOKIE['counter'])) {
    // Если это первое обновление страницы, то устанавливаем значение счетчика равным 1
    $counter = 1;
} else {
    // Если это не первое обновление страницы, то читаем значение счетчика из файла
    $counter = file_get_contents('counter.txt');
}

// Увеличиваем значение счетчика на 1
$counter++;

// Записываем новое значение счетчика в файл
file_put_contents('counter.txt', $counter);

// Выводим значение счетчика
echo "Количество посещений страницы: " . $counter;
?>